for(var i = 0; i < 3; i++) {
    console.log(i);
    console.log("....")
}

// 1st step: i =0, i < 3 (TRUE) -> console.log(0) -> i = 1
// 2nd step: i =1, i < 3 (TRUE) -> console.log(1) -> i = 2
// 3rd step: i =2, i < 3 (TRUE) -> console.log(2) -> i = 3
// 4th step: i =3, i < 3 (FALSE) -> exit the loop
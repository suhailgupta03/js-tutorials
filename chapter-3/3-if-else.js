var x = 100;
var y = 200;

/**
 * Curly braces are not mandatory if there is only one statement
 * They are only mandatory, if there are more than one statements
 * inside if or else block
 */
if (y > x) // 200 > 100 
    console.log('y is greater than x');
else
    console.log('y is less than x');

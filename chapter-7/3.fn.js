/**
 * Following is a function definition:
 *  - that adds two numbers
 *  - has x and y as parameters
 *  - and prints the result
 */
function sum(x, y) {
    /**
     * x and y are called parameters or arguments
     */
    var z = x + y;
    return z;
}

// sum(1,2) is an expression that evaluates to 3
// sum(1,2) => 3
var result = sum(1,2);
console.log(result);